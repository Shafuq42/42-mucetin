char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int		i;
	int		b;

	i = 0;
	b = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (b == 1)
				str[i] -= 32;
		b = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			b = 0;
		else
			b = 1;
		i++;
	}
	return (str);
}
